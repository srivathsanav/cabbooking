package com.srivathsan.cabbooking.data;

public class Driver {

    private int id;
    private int score;

    public int getId() {
        return id;
    }

    public Driver(int id) {
        this.id = id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
